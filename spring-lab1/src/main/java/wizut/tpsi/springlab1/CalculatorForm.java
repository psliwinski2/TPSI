package wizut.tpsi.springlab1;

public class CalculatorForm {

	private Integer x;
	private Integer y;
	private String op;
	private Integer wynik;

	public Integer getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}

	public String getOp() {
		return op;
	}

	public void setOp(String op) {
		this.op = op;
	}

	public Integer getWynik() {
		return wynik;
	}

	public void setWynik(Integer wynik) {
		this.wynik = wynik;
	}

}
